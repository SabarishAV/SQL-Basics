-- String Functions

SELECT UPPER("Hello") AS UpperCase_String

SELECT LOWER("HELLO") AS LowerCase_String

SELECT LENGTH("Hello World") AS Length

SELECT REVERSE("Hello") AS Reverse


-- Date Functions

SELECT DAY("2024-01-02") AS Day

SELECT MONTH("2024-01-02") AS Month

SELECT YEAR("2024-01-02") AS Year



-- Numeric Functions

SELECT ABS(-456)

SELECT COS(2)

-- Retuns the integer by removing value after the .
SELECT FLOOR(25.5)

-- Rounds
SELECT CEIL(25.5)

SELECT LOG(2)